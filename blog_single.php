    <h1>Voo atrasado ou cancelado: o que devo fazer? Tenho direito a indenização?</h1>
    <h3 class="link_blog_single"><i class="fa-regular fa-calendar-days me-2"></i>dezembro 8, 2023 <i class="fa-solid fa-clock ms-3 me-2"></i>4:39 pm</h3>

    <p>
      Os direitos dos passageiros são assegurados, em sua maioria, pelo Código de Defesa do Consumidor e pela Agência Nacional de Aviação Civil, que definem as condutas a serem seguidas pelas companhias aéreas, visando proteger quem pretende voar, inclusive em casos de imprevistos como atrasos ou cancelamentos de voos.<br><br>

      <img src="images/img14.jpeg" class="img-fluid rounded-1 w-100 mb-4" alt="">

      Quais medidas devem ser adotadas pela companhia aérea em caso de atraso ou cancelamento de voo?<br><br>

      Ao ter ciência do atraso ou cancelamento de voo, as companhias aéreas devem imediatamente:<br><br>

      Notificar o consumidor;
      Manter o passageiro informado a cada 30 minutos quanto à previsão de partida dos voos atrasados;
      Oferecer reacomodação, reembolso integral ou execução do serviço por outra modalidade de transporte ou por outra empresa;
      Obs.: em casos de atraso no voo superior a 4 horas ou cancelamento, fica a cargo do passageiro escolher uma dessas opções.<br><br>

      Oferecer gratuitamente assistência material, conforme o tempo de espera:<br><br>
      • atraso a partir de 1h: comunicação (internet, telefone etc.);<br>

      • atraso a partir de 2h: alimentação (voucher, refeição, lanche etc.);<br>

      • atraso a partir de 4h: hospedagem em caso de pernoite no aeroporto e transporte de ida e volta.<br><br>

      Caso o passageiro esteja no local de seu domicílio, a empresa poderá oferecer apenas o transporte para sua residência e de sua residência para o aeroporto.<br><br>

      O Passageiro com Necessidade de Assistência Especial (Pnae) e seus acompanhantes possuem direito à hospedagem independente da exigência de pernoite no aeroporto.<br><br>

      Como agir quando a companhia aérea não oferecer esses serviços de compensação?<br><br>

      De acordo com a Agência Nacional de Aviação Civil – Anac, quando a companhia aérea não oferece os serviços de assistência mencionados no tópico anterior, comete o descumprimento do contrato de transporte aéreo.<br><br>

      Diante de casos assim, os passageiros são orientados a primeiramente buscar os canais de atendimento eletrônico, telefônico ou presencial da própria empresa aérea para tentar resolver o problema de forma administrativa.<br><br>

      Caso não tenha a resolução satisfatória, é aconselhado que o passageiro registre uma reclamação na plataforma “Consumidor” do governo federal. As notificações registradas no referido canal são analisadas pela Anac para direcionar a regulação e fiscalização das Condições Gerais de Transporte Aéreo e da acessibilidade.<br><br>

      Além disso, é recomendado que o passageiro que teve seus direitos desrespeitados pela companhia aérea busque um advogado de sua confiança para auxiliar na obtenção de eventuais reparações por danos sofridos.<br><br>

      <strong>Quando tenho direito a indenização?</strong><br><br>

      Danos materiais: Os passageiros podem reivindicar o reembolso pelos danos materiais sofridos, a exemplo de gastos com alimentação, bebidas e transporte, após um problema com atraso ou cancelamento de voo, conforme determina a legislação brasileira.<br><br>

      Danos morais: Além disso, se o consumidor sofrer prejuízos imateriais com o atraso ou cancelamento do voo, como a perda de um evento importante ou de uma entrevista de emprego, ou por qualquer outro motivo desejar receber alguma indenização por danos morais além do reembolso, deve entrar em contato com uma advogada de sua confiança para auxiliar na obtenção de eventual indenização no Poder Judiciário.<br><br>

